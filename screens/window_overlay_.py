class MessageList(Widget):
    """Reads app.messages, renders everything"""

    def render(self) -> RenderableType:
        """Called by Textual whenever refresh() is triggered"""
        parts = []
        for msg in self.app.messages:
            if msg["type"] == "user":
                parts.append(Text(f"‚Äļ {msg['content']}", style="bold"))
            elif msg["type"] == "text":
                parts.append(Markdown(msg["content"]))
            elif msg["type"] == "thinking":
                parts.append(Text(f"ūüí≠ {msg['content']}", style="dim italic"))
            elif msg["type"] == "tool_call":
                if msg["result"] is None:
                    parts.append(Text(f"ūüĒß {msg['name']} ‚čĮ", style="yellow"))
                else:
                    parts.append(Text(f"‚úď {msg['name']}", style="green"))
                    parts.append(Text(f"  {msg['result']}", style="dim"))
            elif msg["type"] == "error":
                parts.append(Text(f"‚ö†ÔłŹ {msg['content']}", style="red"))
            elif msg["type"] == "approval":
                parts.append(Text(f"‚ö†ÔłŹ Approve? {msg['name']}", style="bold yellow"))
                parts.append(Text(f"  {msg['input']}", style="dim"))
        return Group(*parts) if parts else Text("")
